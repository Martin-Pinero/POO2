package ar.edu.unq.poo2.tp0;

public class punto {
	
	private float x;
	private float y;
	
	
	
	public float getX() {
		return x;
	}



	public void setX(float x) {
		this.x = x;
	}



	public float getY() {
		return y;
	}



	public void setY(float y) {
		this.y = y;
	}



	public punto(float x, float y) {
		super();
		this.x = x;
		this.y = y;
	}
}
